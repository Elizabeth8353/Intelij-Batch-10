package com.MyNotes.Println;

public class PrintLine {
    public static void main(String[] args) {
        System.out.println("The combination of certain characters together behaves differently inside the double quotes");

        System.out.print("HI");
        System.out.println("hello");//will make new line for HEY
        System.out.println("Hey");

        // to move curser to next line \n inside double quotes
        System.out.println("This name Eli will be on next line \n"+"Eli");

        //to make 8 spaces \t
        System.out.println("This will make Bri after 8 spaces \t"+"Bri");



    }
}
